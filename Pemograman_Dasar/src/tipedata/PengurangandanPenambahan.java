        System.out.println("x :" + x);
        System.out.println("y :" + y);
        z = --x;
        System.out.println("diket x : 42");
        System.out.println("dan z : --x");
        System.out.println("maka hasil x,z");
        System.out.println("x :" + x);
        System.out.println("z :" + z);
        
